a = int(input("Enter a number : "))
b = int(input("Enter a number : "))
c = int(input("Enter a number : "))
d = int(input("Enter a number : "))

sq = (a**2)
sq1 = (b**2)
sq2 = (c**2)
sq3 = (d**2)

print("Square of", a, "is", sq)
print("Square of", b, "is", sq1)
print("Square of", c, "is", sq2)
print("Square of", d, "is", sq3)

